#include "libft.h"

/*
**	Alloue (avec malloc(3)) et retourne une copie de la chaine passée en
**	paramètre sans les espaces blancs au debut et à la fin de cette chaine. On
**	considère comme espaces blancs les caractères ’ ’, ’\n’ et ’\t’. Si s ne
**	contient pas d’espaces blancs au début ou à la fin, la fonction renvoie
**	une copie de s. Si l’allocation echoue, la fonction renvoie NULL.
*/

char	*ft_strtrim(char const *s)
{
	size_t	i;
	size_t	j;
	size_t	k;

	if (!s)
		return (NULL);
	i = 0;
	while (s[i] == ' ' || s[i] == '\n' || s[i] == '\t')
		i++;
	j = 0;
	k = 0;
	while (s[k])
	{
		if (s[k] != ' ' && s[k] != '\n' && s[k] != '\t')
		{
			k++;
			j = k;
		}
		else
			k++;
	}
	if (j == 0)
		return (ft_strdup(""));
	return (ft_strsub(s, i, j - i));
}
